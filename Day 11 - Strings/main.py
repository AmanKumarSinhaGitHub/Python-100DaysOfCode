# String in Double Quote
print("Aman")

# String in Single Quote
print('Python')

# String in Double Quote within Single Quote
print('I am Learning "Python". It is really amazing.')

# Multiline String
print("""Hello,
I am Aman.
I am Learing Python.""")

# Accessing Character of a String

name = "Python"
print(name[0]) # Output : P
print(name[1]) # Output : y
print(name[2]) # Output : t
print(name[3]) # Output : h
print(name[4]) # Output : o
print(name[5]) # Output : n

# Note : Index start from zero (0)

# Accessing String characters using LOOPS
print("Printing the same thing using LOOPS")
for character in name:
    print(character)
    
